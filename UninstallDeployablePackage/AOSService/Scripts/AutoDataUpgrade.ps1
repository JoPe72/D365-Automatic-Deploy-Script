$config = @{}
$configFilePath = Join-Path $PSScriptRoot 'AutoDataUpgrade.config'
if (Test-Path $configFilePath)
{
    $configText = Get-Content "$configFilePath" -Raw
    $config = ConvertFrom-Json $configText
}

function InvokePostScript($postScriptFile)
{
    if ($postScriptFile)
    {
        $postScriptPath = Join-Path $PSScriptRoot $postScriptFile
        if (Test-Path $postScriptPath)
        {
            Write-Output "Executing script '$postScriptFile'."
            Invoke-Expression $postScriptPath
        }
    }
}

#patch initial schema
if (!($config.InitialSchema.Skip))
