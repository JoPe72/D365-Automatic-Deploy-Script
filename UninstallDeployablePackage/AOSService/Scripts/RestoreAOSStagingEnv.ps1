

$ErrorActionPreference = 'Stop'
Import-Module "$PSScriptRoot\CommonRollbackUtilities.psm1" -Force -DisableNameChecking
Import-Module "$PSScriptRoot\AosEnvironmentUtilities.psm1" -Force -DisableNameChecking



Try
{

    $aosWebServiceBackupPath = Get-AosServiceBackupPath
    $aosWebServicePath = Get-AosServicePath

    # $RunbookBackupFolder and $PSScriptRoot variables are populated directly from runbook.
    $DPBackupFolder = "$RunbookBackupFolder"

    if ([string]::IsNullOrEmpty($DPBackupFolder))
    {
        $DPBackupFolder = "$PSScriptRoot\ManualAosServiceBackup"
    }
    if(Test-Path $(Join-Path $DPBackupFolder "AosServiceBackup"))
    {
        if(Test-Path $aosWebServiceBackupPath)
        {
            get-childitem -path "$aosWebServiceBackupPath" -Recurse | remove-item -force -recurse
            Remove-Item $aosWebServiceBackupPath -Force
        }
        Move-Item -Path $(Join-Path $DPBackupFolder "AosServiceBackup") -Destination $aosWebServiceBackupPath -Force
    }
    if(Test-Path $aosWebServiceBackupPath)
    {
        Rename-Item $aosWebServicePath "AosServiceStaging"
        Rename-Item $aosWebServiceBackupPath "AosService"

        GenerateMetadataModuleInstallationInfo
    }
}
Catch
{
    throw $_
}



