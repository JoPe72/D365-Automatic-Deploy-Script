param
(
    [Parameter(Mandatory=$true)]
    $config,
    [Parameter(Mandatory=$true)]
    $log
)

Import-Module "$PSScriptRoot\AosCommon.psm1" -Force -DisableNameChecking
Initialize-Log $log

Write-Log "Decoding settings"
$settings = Decode-Settings $config

Write-Log "Constructing private URL"
$publicUrl = $settings.'Infrastructure.HostUrl'
$privateUrl = Construct-PrivateAosInstanceUrl $publicUrl
$uri = New-Object Uri($privateUrl) 

$IsPrivateAOSInstance = $settings.'Infrastructure.IsPrivateAOSInstance'
if (![System.String]::IsNullOrWhiteSpace($IsPrivateAOSInstance) -and [System.Convert]::ToBoolean($IsPrivateAOSInstance))
{       
    Write-Log "This a private AOS instance, creating a new binding."

    $port = $settings.'Infrastructure.PublicIPEndpointPort'
    $protocol = 'https'
    $siteName = $settings.'Infrastructure.ApplicationName'

    Write-Log "Looking for binding with Name [$siteName], IPAddress [*], Protocol [$protocol], Port [$port], HostHeader [$($uri.DnsSafeHost)]."
    $existingBinding = Get-WebBinding -Name $siteName -IPAddress * -Protocol $protocol -Port $port -HostHeader $($uri.DnsSafeHost) -ErrorAction SilentlyContinue
    if ($existingBinding -eq $null)
    {
        Write-Log "Creating new binding."
        New-WebBinding -Name $siteName -IPAddress * -Protocol $protocol -Port $port -HostHeader $($uri.DnsSafeHost)
        Write-Log "New binding successfuly created."
    }
    else
    {
        Write-Log "The binding already exists."
    }
}

if (![System.String]::IsNullOrWhiteSpace($settings.'Infrastructure.PrivateAOSInstanceIp'))
{   
    Write-Log "Updating web.config"

    $AosWebRoot = $settings.'Infrastructure.WebRoot'
    Write-Log "Reading web.config at $AosWebRoot"

    $WebConfigPath = "$AosWebRoot\web.config"
    [xml]$web = Get-Content "$WebConfigPath"
    $add = $web.configuration.appSettings.add | where key -eq 'Infrastructure.PrivateAOSInstanceUrl' | select -First 1
    $add.value = $privateUrl
    $web.Save("$WebConfigPath")
}

Write-Log "Finished, exiting."
