param
(
    [Parameter(Mandatory=$true)]
    $config,
    [Parameter(Mandatory=$true)]
    $log
)

Import-Module "$PSScriptRoot\AosCommon.psm1" -Force -DisableNameChecking
Initialize-Log $log
Write-Log "Decoding settings"
$settings = Decode-Settings $config

$IsPrivateAOSInstance = $settings.'Infrastructure.IsPrivateAOSInstance'
if ([System.String]::IsNullOrWhiteSpace($IsPrivateAOSInstance) -or ![System.Convert]::ToBoolean($IsPrivateAOSInstance))
{       
    Write-Log "Setting up Microsoft Dynamics 365 Unified Operations: Batch service"

    $aosSitePath = $settings.'Infrastructure.WebRoot'

    # Register the Batch Service
    $StartBatchCommand = "$PSScriptRoot\BatchSetup.ps1 -rootPath `"$aosSitePath`" -webRootPath `"$aosSitePath`""
    Write-Log "Calling $StartBatchCommand"
    $output = Invoke-Expression $StartBatchCommand
    if (!$?)
    {
        Write-Log "Start batch output: $output"
        Write-Log "last error: $($error[0])"
        throw $error[0]
    }

    Write-Log "Start batch output: $output"
}
else
{
    Write-Log "Running on private AOS instance, skipping Microsoft Dynamics 365 Unified Operations: Batch service setup."
}

Write-Log "Script completed"

