Initialize-Log $log

Write-Log "Decoding settings"
$settings = Decode-Settings $config

$command = Resolve-Path "$($settings.'Infrastructure.WebRoot')\bin\Microsoft.Dynamics.AX.Framework.ConfigEncryptor.exe"

$commandParameter = " -encrypt `"$($settings.'Infrastructure.WebRoot')\web.config`""

Write-Log "Config encryption command: $command $commandParameter"

