param
(
    [Parameter(Mandatory=$true)]
    $config,
    [Parameter(Mandatory=$true)]
    $log
)

Import-Module "$PSScriptRoot\AosCommon.psm1" -Force -DisableNameChecking

$DefaultCompanyBanner = "DefaultCompanyBanner.jpg"

function Replace-DefaultCompanyBanner($source, $targetDir)
{
    cp $source "$targetDir\$DefaultCompanyBanner" -Force
}

Initialize-Log $log
Write-Log "Decoding settings"
$settings = Decode-Settings $config

$brandingBannerJson = $($settings.'LCS.BrandingBanner')

if ($brandingBannerJson -eq $null)
{
    exit 0
}

$brandingBannerData = ConvertFrom-Json $brandingBannerJson
$source = [IO.Path]::Combine($brandingBannerData.DestinationDirectory, $brandingBannerData.DestinationFileName)

if (![IO.File]::Exists($source))
{
    exit 0
}

$webroot = $($settings.'Infrastructure.WebRoot')
$packagedir = $($settings.'Aos.PackageDirectory')
$targetDirs = "$webroot\Resources\Images", "$packagedir\ApplicationPlatform\ApplicationPlatform\AxResource\ResourceContent\Images"

foreach ($targetDir in $targetDirs)
{
    Write-Log "Replacing $DefaultCompanyBanner at $targetDir"
    Replace-DefaultCompanyBanner $source $targetDir
}
