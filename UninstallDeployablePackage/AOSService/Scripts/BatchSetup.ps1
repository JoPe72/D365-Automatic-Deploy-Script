Param(
  [Parameter(Mandatory=$True)]
  [string]$rootPath,
  [Parameter(Mandatory=$false)]
  [string]$webRootPath,
  [Parameter(Mandatory=$false)]
  [string]$ServiceAccountName = "NT AUTHORITY\NETWORK SERVICE",
  [Parameter(Mandatory=$false)]
  [string]$ServiceAccountPassword = $([GUID]::NewGuid()).ToString()
)

try
{
	$error.Clear()
	Write-Output $rootPath

	# Construct path.
	if (![String]::IsNullOrWhiteSpace($webRootPath))
	{
	    $aosSitePath = $webRootPath
	}
	else
	{
	    $aosSitePath = "$rootPath\Dobind\Packages\Cloud\AosWebApplication\AosWebApplication.csx\roles\AosWeb\approot"
	}

	# Register the Batch Service
	$service = Get-WmiObject -Class Win32_Service -Filter "Name='DynamicsAxBatch'"
	if($service -ne $null)
	{
	    $service.delete()
	}

	$webconfigPath = $aosSitePath + "\web.config" #"e:\webconfig.xml"
	Write-Output $webconfigPath
	$batchExePath = $aosSitePath + "\bin\Batch.exe"
	Write-Output $batchExePath
   
        $Password = ConvertTo-SecureString $ServiceAccountPassword -AsPlainText -Force
        $Creds = New-Object System.Management.Automation.PSCredential($ServiceAccountName, $Password)
	New-Service -name "DynamicsAxBatch"`
                -binarypathname "$batchExePath -service `"$webConfigPath`""`
                -Description "Polls and executes Unified Operations batch requests."`
                -DisplayName "Microsoft Dynamics 365 Unified Operations: Batch Management Service"`
                -StartupType Automatic `
                -Credential $Creds

	Write-Output "Setting batch failure conditions"
	sc.exe failure DynamicsAxBatch reset= 30 actions= restart/30000
	reg add HKEY_LOCAL_MACHINE\SYSTEM\CurrentControlSet\Services\DynamicsAxBatch /v DelayedAutostart /t REG_DWORD /d 1 /f
	Write-Output "Starting batch service"
	start-service DynamicsAxBatch
	Write-Output "Script completed"
	Write-Output $error
}
catch
{
	Write-Output $error
}
