Import-Module WebAdministration

$ErrorActionPreference = "stop"
if(Test-Path -Path "$PSScriptRoot\License")
{
    $files=get-childitem -Path:$PSScriptRoot\License *.* | sort Name
    foreach ($file in $files) 
    {
        if($file.Name -ne "place your license file here.txt")
        {
            $deploymentSetupParameter = "-setupmode importlicensefile -licensefilename `"$($file.FullName)`""
            $logfile = "$PSScriptRoot\importLicense.log"
            $errorfile = "$PSScriptRoot\importLicense.error.log"

            $deploySetupPs1 = "$PSScriptRoot\TriggerDeploymentSetupEngine.ps1"

            invoke-Expression  "$deploySetupPs1 -deploymentSetupParameter:'$deploymentSetupParameter' -logfile:`"$logfile`" -errorfile:`"$errorfile`""

            Write-Output "Loading license $($file.FullName) completed."
        }

    }
}



