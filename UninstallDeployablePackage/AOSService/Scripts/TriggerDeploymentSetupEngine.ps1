[CmdletBinding()]
param
(
    [Parameter(Mandatory=$true)]
    [string]$deploymentSetupParameter,

    [Parameter(Mandatory=$true)]
    [string]$logfile,

    [Parameter(Mandatory=$true)]
    [string]$errorfile
)

Import-Module WebAdministration
Import-Module "$PSScriptRoot\CommonRollbackUtilities.psm1" -DisableNameChecking
Import-Module "$PSScriptRoot\AosEnvironmentUtilities.psm1" -Force -DisableNameChecking

$ErrorActionPreference = "stop"


$webroot = Get-AosWebSitePhysicalPath
$aosSiteName = Get-AosWebSiteName
$aosAppPoolName = Get-AosAppPoolName
Stop-WebSite $aosSiteName
if((Get-WebAppPoolState $aosAppPoolName).Value -ne 'Stopped') 
{
    Stop-WebAppPool $aosAppPoolName
}

$command = "$webroot\bin\Microsoft.Dynamics.AX.Deployment.Setup.exe"

$commandParameter = "-bindir `"$(Get-CommonBinDir)`""
$commandParameter += " -metadatadir `"$(Get-AOSPackageDirectory)`""
$commandParameter += " -sqluser `"$(Get-DataAccessSqlUsr)`""
$commandParameter += " -sqlserver `"$(Get-DataAccessDbServer)`""
$commandParameter += " -sqldatabase `"$(Get-DataAccessDatabase)`""
$commandParameter += " $deploymentSetupParameter"

$DBServer = $(Get-DataAccessDbServer)
if($DBServer -eq ".")
{
    $commandParameter += " -isazuresql `"false`""
}
elseif( [System.Environment]::MachineName.Equals($DBServer.Split('.')[0], [StringComparison]::OrdinalIgnoreCase))
{
    $commandParameter += " -isazuresql `"false`""
}
else
{
    $commandParameter += " -isazuresql `"true`""
}

$commandParameter += " -logfilename `"$logfile`""

Write-Output "executing command: $command $commandParameter"

$commandParameter += " -sqlpwd `"$(Get-DataAccessSqlPwd)`""

$process = Start-Process $command $commandParameter -PassThru -Wait -RedirectStandardOutput "$logfile.output.log" -RedirectStandardError "$errorfile"

Write-Output "Log file location: $logfile"

$deploymentSetupError = Get-Content "$errorfile"
if ($process.ExitCode -ne 0) {
    throw $deploymentSetupError
}

Write-Output "finish triggering deployment setup engine."




