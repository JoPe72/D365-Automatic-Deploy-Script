{
    Write-Output "Running as admin"
    exit
}

# Check if registry has a dev configuration setup
$regPath = "HKCU:\SOFTWARE\Microsoft\Dynamics\AX7\Development\Configurations"
$regKey = Get-ItemProperty -Path $regPath
if ($regKey -ne $null)
{
    $devConfigCurrent = $regKey.CurrentConfig
    if ($devConfigCurrent)
    {
        $devConfigPath = $regKey.($devConfigCurrent)
    }
}

# If nothing in the registry, check if the default path has a configuration file
if (!($devConfigPath))
{
    $devConfigPath = Join-Path -Path "$($env:HOMEDRIVE)\$($env:HOMEPATH)" -ChildPath "Documents\Visual Studio 2015\Settings\DynamicsDevConfig.xml"
}

if (Test-Path $devConfigPath)
{
    Write-Output "Using development tools configuration"
    Import-Module "$PSScriptRoot\NonAdminDevTools.psm1" -DisableNameChecking
}
else
{
    Write-Output "No development configuration found"
}
