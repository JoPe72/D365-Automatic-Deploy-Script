################################################################################
# Deploy AX reports To SSRS server
################################################################################
param
(
    [Parameter(Mandatory=$true)]
    [string]$config,
    [Parameter(Mandatory=$true)]
    [string]$log
)

try
{
    Import-Module "$PSScriptRoot\AosCommon.psm1" -Force -DisableNameChecking
    Import-Module "$PSScriptRoot\Reporting.psm1" -Force -DisableNameChecking

    # Initialize the log file
    Initialize-Log $log

    # Deploy AX Reports
    Deploy-AxReport -config $config -log $log
    return 0
}
catch
{
    $catchedException = $_.Exception
    $errMsg = $($_.Exception.ToString())
    Add-Content -Path $log -Value $errMsg
    Write-Log $("Error: Failed to Deploy AX Reports.")
    throw $catchedException
}
